SNAKE_POINT JHSnake::getNextPos()
{
	SNAKE_POINT	sp;
	sp = getHead();

	switch (m_nDir)
	{
		case DIR_U:
		{
			if (sp.y == 0)
			{
				sp.y = FIELD_SIZE_Y - 1;
			}
			else
			{
				sp.y--;
			}
		}
		break;

		case DIR_R:
		{
			if (sp.x == (FIELD_SIZE_X - 1))
			{
				sp.x = 0;
			}
			else
			{
				sp.x++;
			}
		}
		break;

		case DIR_D:
		{
			if (sp.y == (FIELD_SIZE_Y - 1))
			{
				sp.y = 0;
			}
			else
			{
				sp.y++;
			}
		}
		break;

		case DIR_L:
		{
			if (sp.x == 0)
			{
				sp.x = (FIELD_SIZE_X - 1);
			}
			else
			{
				sp.x--;
			}
		}
		break;
	}

	return sp;
}

