void JHSnake::setSpecialPos()
{
	srand(time(NULL));

	int		nX;
	int		nY;
	bool	bSet = false;

	SNAKE_POINT spSnake = getHead();

	do
	{
		nX = rand() % FIELD_SIZE_X;
		nY = rand() % FIELD_SIZE_Y;

		bSet = false;

		if ((g_nMaze[getMaze()][nY][nX] == BLOCK_WALL)	||
			((nX == spSnake.x) && (nY == spSnake.y))	||
			((nX == m_spFood.x) && (nY == m_spFood.y)))
		{
			bSet = true;
		}
	}
	while (bSet);

	m_spSpecial.x = nX;
	m_spSpecial.y = nY;
}

