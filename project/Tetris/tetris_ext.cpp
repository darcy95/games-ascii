void JHTetrisExt::OnLineCompleteAct(int nLine)
{
	if (m_nBlockCount < BLOCK_COUNT)
	{
		m_nBlockCount++;
	}
	else
	{
		if (m_nMovePoint > 0)
		{
			m_nMovePoint--;
		}

		m_nBlockCount = 0;
	}
}

